/*
 * adxl345.h  —  ADXL345 3축 가속도계 드라이버 (MPU6050 대체)
 *
 * 자이로 없음 → 가속도 기반 tilt 계산 + 저역통과 필터
 * pitch/roll 단위: 정수 도(°), 범위 -90 ~ +90
 */

#ifndef INC_ADXL345_H_
#define INC_ADXL345_H_

#include "main.h"

/****************************************************************
    I2C 주소 (SDO 핀 = GND → 0x53, SDO = VCC → 0x1D)
****************************************************************/
#define ADXL345_ADDR             (0x53 << 1)   /* HAL은 8-bit 주소 사용 */

/****************************************************************
    레지스터 맵
****************************************************************/
#define ADXL345_REG_DEVID        0x00
#define ADXL345_REG_BW_RATE      0x2C
#define ADXL345_REG_POWER_CTL   0x2D
#define ADXL345_REG_DATA_FORMAT  0x31
#define ADXL345_REG_DATA_X0      0x32   /* X0, X1, Y0, Y1, Z0, Z1 연속 6바이트 */

#define ADXL345_DEVID_VAL        0xE5

/****************************************************************
    스케일 팩터
    Full Resolution 모드(±2g): 256 LSB/g (4mg/LSB)
****************************************************************/
#define ADXL345_SCALE            256.0f

/****************************************************************
    저역통과 필터 계수
    alpha = 현재 측정값 반영 비율 (0 = 최대 필터링, 1 = 필터 없음)
    호출 주기 10ms 기준 0.3 = 적당한 평활화
****************************************************************/
#define ADXL345_LPF_ALPHA        0.3f

/****************************************************************
    함수 선언 (MPU6050과 동일한 인터페이스)
****************************************************************/
HAL_StatusTypeDef ADXL345_Init(I2C_HandleTypeDef *hi2c);
HAL_StatusTypeDef ADXL345_GetAngle(I2C_HandleTypeDef *hi2c,
                                    int16_t *pitch, int16_t *roll);

#endif /* INC_ADXL345_H_ */
